"""
Operadores aritméticos(binários)
Somar(+)
Subtrair(-)
Multiplicar(*)
Dividir(/)
Exponenciação(**)
Módulo(%) -> resto da divisão
"""

numero1 = 10
numero2 = 3

resultado = numero1 + numero2
print(resultado)

#Operadores Unários
valor = 10
valor += 1
print(valor)

"""
Precedencia de operadores
0) Parenteses (ele dá uma prioridade a mais)
1) Multiplicação e divisao
2) Adicao e subtração
"""
resultado = 2 + 2 * 2 / 3
print(resultado)